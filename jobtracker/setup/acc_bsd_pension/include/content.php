<div class="introduction">
    <ul>
        <li>An accounts based pension is an income stream that the Trustee of the Fund may pay to a member of the Fund who has met a condition of release such as retirement.</li> 
        <li>It is called an Account Based Pension as the Trustee invests the assets of the Fund being held for the Pension Member of the Fund and any returns are allocated to that Member’s account.</li> 
        <li>At the same time the Pension Member must take a minimum amount from the Member’s Account.</li> 
        <li>There is no maximum pension amount and the member may commute the pension to a lump sum which will also be deduction from the member’s account.</li> 
        <li>The commutation may also be rolled back to the member’s accumulation account to minimise the pension payment requirements.</li>
   </ul>    
</div>
